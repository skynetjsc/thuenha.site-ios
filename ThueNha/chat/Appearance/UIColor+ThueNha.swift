import Foundation

extension UIColor {
    
    class var thueNhaWhite: UIColor {
        return UIColor.white
    }
    
    class var thueNhaBlack: UIColor {
        return UIColor(hex: "#4A4A4A")
    }
    
    class var thueNhaOrange: UIColor {
        return UIColor(hex: "#FB7A32")
    }
    
    class var thueNhaYellow: UIColor {
        return UIColor(hex: "#FFD15C")
    }

}
